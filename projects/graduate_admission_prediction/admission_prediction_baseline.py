"""대학원 합격 여부 예측 기본 실습.

이 파일의 목표
- gpascore.csv 파일을 불러온다.
- GRE, GPA, rank 값을 입력으로 사용한다.
- admit 값을 정답으로 사용해서 합격 여부를 예측하는 모델을 학습한다.

주의
이 코드는 '성능 좋은 완성 모델'이 아니라, 딥러닝 분류 모델의 흐름을 익히기 위한 기본 예제입니다.
그래서 일부러 정규화, 원-핫 인코딩, 검증 데이터 분리 같은 개선은 넣지 않았습니다.
파일 아래쪽에 왜 성능이 좋지 않을 수 있는지 따로 정리해두었습니다.
"""

import pandas as pd
import numpy as np
import tensorflow as tf


# 1. 데이터 불러오기
# CSV 파일은 표 형태의 데이터를 저장하는 파일입니다.
# 이 프로젝트에서는 각 행이 한 명의 지원자 정보를 의미합니다.
#
# 컬럼 의미
# - admit: 합격 여부입니다. 1이면 합격, 0이면 불합격입니다.
# - gre: GRE 시험 점수입니다.
# - gpa: 학점입니다.
# - rank: 지원한 학교의 등급입니다. 보통 숫자가 낮을수록 더 좋은 등급으로 봅니다.
data = pd.read_csv("gpascore.csv")

# head()는 데이터의 맨 앞 5줄을 보여줍니다.
# CSV가 제대로 불러와졌는지 확인할 때 자주 사용합니다.
print("\n[데이터 앞부분 확인]")
print(data.head())


# 2. 결측치 확인과 처리
# 결측치는 비어 있는 값입니다.
# 예를 들어 어떤 학생의 GPA가 비어 있으면 모델 학습 중 문제가 생길 수 있습니다.
print("\n[컬럼별 결측치 개수 - 처리 전]")
print(data.isnull().sum())

# dropna()는 결측치가 들어 있는 행을 제거합니다.
# 입문 단계에서는 가장 단순하고 이해하기 쉬운 처리 방법입니다.
# 단점은 데이터 수가 줄어든다는 점입니다.
data = data.dropna()

print("\n[컬럼별 결측치 개수 - 처리 후]")
print(data.isnull().sum())

# fillna()는 결측치를 다른 값으로 채울 때 사용합니다.
# 지금은 위에서 dropna()를 했기 때문에 실제로 채울 결측치가 거의 없습니다.
# 그래도 '결측치를 평균값으로 채우는 방법도 있다'는 공부용 예시로 남겨둡니다.
data.fillna(data.mean(), inplace=True)

# describe()는 평균, 표준편차, 최솟값, 최댓값 같은 통계 정보를 보여줍니다.
# 데이터 범위가 어느 정도인지 볼 때 좋습니다.
print("\n[GPA 통계 확인]")
print(data["gpa"].describe())


# 3. 정답 데이터 만들기
# y_train은 모델이 맞혀야 하는 정답입니다.
# 여기서는 합격 여부인 admit 컬럼을 정답으로 사용합니다.
#
# to_numpy()를 쓰는 이유:
# TensorFlow 모델은 pandas DataFrame보다 numpy 배열 형태의 입력을 다루기 쉽습니다.
y_train = data["admit"].to_numpy()

print("\n[정답 데이터 y_train 확인]")
print("y_train 모양:", y_train.shape)
print("앞 10개 정답:", y_train[:10])


# 4. 입력 데이터 만들기
# x_train은 모델에게 보여줄 입력값입니다.
# 여기서는 GRE, GPA, rank 세 가지 정보만 사용합니다.
#
# 모델은 이 세 숫자를 보고 admit이 0인지 1인지 예측하려고 합니다.
# 예: [gre, gpa, rank] -> admit
x_train = data[["gre", "gpa", "rank"]].to_numpy()

print("\n[입력 데이터 x_train 확인]")
print("x_train 모양:", x_train.shape)
print("앞 5개 입력:\n", x_train[:5])


# 5. iterrows로 직접 입력 데이터 만들어보기
# 위에서는 pandas 기능으로 한 번에 x_train을 만들었습니다.
# 아래 코드는 같은 데이터를 반복문으로 직접 만드는 연습용 코드입니다.
#
# 실제 학습에는 위에서 만든 x_train을 사용합니다.
# x_train1은 '행을 하나씩 돌면서 리스트에 넣으면 이런 식으로 만들 수 있다'는 확인용입니다.
x_train1 = []

for i, rows in data.iterrows():
    # i는 행 번호(index)입니다.
    # rows는 해당 행의 데이터입니다.
    #
    # 원래 모든 행을 print하면 출력이 너무 많아져서 중요한 결과가 묻힙니다.
    # 그래서 반복문 안에서는 데이터를 리스트에 담기만 하고, 아래에서 일부만 확인합니다.
    x_train1.append([rows["gre"], rows["gpa"], rows["rank"]])

print("\n[반복문으로 만든 x_train1 확인]")
print("x_train1 타입:", type(x_train1))
print("앞 5개 입력:", x_train1[:5])


# 6. 딥러닝 모델 만들기
# Sequential은 층을 순서대로 쌓는 가장 단순한 모델 방식입니다.
# 입문 단계에서 모델 구조를 이해하기 좋습니다.
model = tf.keras.models.Sequential([
    # Dense는 모든 입력이 모든 노드와 연결되는 기본 신경망 층입니다.
    # 64는 노드 개수입니다.
    # relu는 중간층에서 자주 사용하는 활성화 함수입니다.
    tf.keras.layers.Dense(64, activation="relu"),

    # 두 번째 Dense 층입니다.
    # 현재 데이터는 약 400개 정도라 사실 128개 노드는 다소 클 수 있습니다.
    # 여기서는 기존 코드의 모델 구조를 유지하고, 아래 주석에서 한계를 설명합니다.
    tf.keras.layers.Dense(128, activation="relu"),

    # 마지막 층은 노드가 1개입니다.
    # 합격 확률 하나를 출력해야 하기 때문입니다.
    # sigmoid는 결과를 0~1 사이 값으로 만들어줍니다.
    # 예: 0.8이면 합격 가능성을 80% 정도로 본다는 뜻입니다.
    tf.keras.layers.Dense(1, activation="sigmoid"),
])


# 7. 모델 학습 설정하기
# compile은 모델을 어떻게 학습할지 정하는 단계입니다.
#
# optimizer='adam'
# - 모델의 가중치를 어떻게 업데이트할지 정합니다.
# - Adam은 많이 쓰이는 기본 optimizer입니다.
#
# loss='binary_crossentropy'
# - admit은 0 또는 1입니다.
# - 이런 이진 분류 문제에서는 binary_crossentropy를 자주 사용합니다.
#
# metrics=['accuracy']
# - 학습 중 정확도를 같이 출력해서 모델이 어느 정도 맞히는지 확인합니다.
model.compile(optimizer="adam", loss="binary_crossentropy", metrics=["accuracy"])


# 8. 모델 학습시키기
# fit은 실제로 모델을 학습시키는 함수입니다.
#
# x_train: 입력 데이터입니다. GRE, GPA, rank가 들어 있습니다.
# y_train: 정답 데이터입니다. admit 값이 들어 있습니다.
# epochs=1000: 같은 데이터를 1000번 반복해서 학습합니다.
#
# 주의:
# 반복 횟수가 많다고 항상 좋은 것은 아닙니다.
# 데이터가 적거나 모델이 크면 중간에 정확도가 흔들리거나 과적합될 수 있습니다.
history = model.fit(x_train, y_train, epochs=1000, verbose=0)

# verbose=0으로 설정했기 때문에 epoch마다 나오는 긴 학습 로그는 숨깁니다.
# 대신 마지막 학습 결과만 직접 출력해서 화면을 깔끔하게 유지합니다.
final_loss = history.history["loss"][-1]
final_accuracy = history.history["accuracy"][-1]

print("\n[학습 완료]")
print(f"최종 loss: {final_loss:.4f}")
print(f"최종 accuracy: {final_accuracy:.4f}")


# 9. 새로운 지원자 예측하기
# 학습이 끝난 모델에 새로운 지원자 정보를 넣어 합격 가능성을 예측합니다.
#
# 입력 형식은 학습 때와 같아야 합니다.
# [gre, gpa, rank]
#
# 아래 예시는 두 명의 지원자를 예측합니다.
# - 첫 번째: GRE 750, GPA 3.70, rank 3
# - 두 번째: GRE 400, GPA 2.20, rank 1
predictions = model.predict([[750, 3.70, 3], [400, 2.2, 1]])

print("\n[예측 결과]")
print(predictions)


# 10. 왜 성능이 좋지 않을 수 있을까?
# 이 프로젝트는 입문용 기본 코드라서 일부러 개선을 넣지 않았습니다.
# 그래서 정확도가 안정적으로 높게 나오지 않을 수 있습니다.
#
# 1) 입력값 스케일 차이가 큽니다.
#    gre는 대략 400~800 범위입니다.
#    gpa는 대략 2~4 범위입니다.
#    rank는 1~4 범위입니다.
#    숫자 범위가 너무 다르면 모델이 어떤 입력을 더 크게 받아들여 학습이 불안정해질 수 있습니다.
#
# 2) 정규화를 하지 않았습니다.
#    정규화는 입력값들의 범위를 비슷하게 맞추는 작업입니다.
#    예를 들어 gre, gpa, rank를 모두 0~1 근처 범위로 맞추면 학습이 더 안정될 수 있습니다.
#    이 파일에서는 기존 코드 흐름을 유지하기 위해 정규화를 넣지 않았습니다.
#
# 3) 데이터 수가 적습니다.
#    이 데이터는 약 400개 정도입니다.
#    딥러닝 모델은 보통 많은 데이터를 볼수록 안정적으로 배웁니다.
#    데이터가 적으면 학습 결과가 쉽게 흔들릴 수 있습니다.
#
# 4) 입력 특성이 적습니다.
#    현재 모델은 gre, gpa, rank 세 가지 정보만 봅니다.
#    실제 대학원 합격에는 추천서, 자기소개서, 연구 경험, 면접, 전공 적합성 같은 정보도 중요할 수 있습니다.
#    이런 정보가 빠져 있으니 정확하게 예측하기 어렵습니다.
#
# 5) 합격 여부 자체가 불확실한 문제입니다.
#    같은 점수와 학점을 가진 지원자라도 학교, 전공, 지원 시기, 경쟁률에 따라 결과가 달라질 수 있습니다.
#    그래서 이 모델의 예측값은 정답이라기보다 '가능성 추정'으로 보는 것이 좋습니다.
#
# 6) rank를 숫자로 그대로 넣는 것이 애매합니다.
#    rank=4가 rank=2보다 나쁜 것은 맞을 수 있지만,
#    rank=4가 rank=2의 정확히 두 배 나쁘다는 뜻은 아닙니다.
#    그런데 모델은 숫자를 그대로 넣으면 이런 순서와 간격을 실제 숫자처럼 받아들일 수 있습니다.
#
# 7) 모델이 데이터에 비해 큽니다.
#    데이터는 약 400개인데 Dense(64), Dense(128)은 꽤 큰 편입니다.
#    작은 데이터에 큰 모델을 쓰면 학습이 불안정하거나, 학습 데이터에만 과하게 맞는 과적합이 생길 수 있습니다.
#
# 8) 검증 데이터를 따로 나누지 않았습니다.
#    지금 코드는 학습 데이터에 대한 정확도만 봅니다.
#    진짜 실력은 처음 보는 데이터에서 얼마나 맞히는지 봐야 합니다.
#    그래서 나중에는 train/test split을 배워서 검증하는 과정이 필요합니다.
#
# 9) 학습 로그상 정확도가 흔들릴 수 있습니다.
#    정확도가 올라갔다가 다시 내려가는 것은 모델이 안정적으로 규칙을 찾지 못하고 있다는 신호일 수 있습니다.
#    위의 스케일 문제, 데이터 부족, 모델 크기 문제가 함께 영향을 줄 수 있습니다.
"""
다음에 개선해볼 수 있는 방향
- GRE, GPA, rank 정규화하기
- rank를 원-핫 인코딩으로 바꾸기
- train/test 데이터 나누기
- Dense 층을 더 작게 만들기
- epochs 줄이기
- 예측 결과를 0~1 확률로 해석하는 방법 익히기
"""

